class Sighting {
  final String id;
  final String title;
  final String location;
  final String date;
  final String description;
  final String threatLevel;
  final double lat;
  final double lng;
  final bool isLocal;

  const Sighting({
    required this.id,
    required this.title,
    required this.location,
    required this.date,
    required this.description,
    required this.threatLevel,
    required this.lat,
    required this.lng,
    this.isLocal = false,
  });

  Sighting copyWith({bool? isLocal}) => Sighting(
    id: id, title: title, location: location, date: date,
    description: description, threatLevel: threatLevel,
    lat: lat, lng: lng, isLocal: isLocal ?? this.isLocal,
  );
}

